marks=float(input("enter your marks:"))
if marks >=90:
    print(f"your grade is: S")
elif marks >=80:
    print("your grade is:A ")
elif marks >=70:
    print("your grade is:B")
elif marks >=60:
    print("your grade is:C")
elif marks >=50:
    print("your grade is :D")
else:
    print(" PROMOTED")            

